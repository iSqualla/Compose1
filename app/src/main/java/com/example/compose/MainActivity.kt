class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            val navController = rememberNavController()
            NavHost(navController = navController, startDestination = "greeting/{name}"){
                composable("greeting/{name}",
                arguments = listOf(navArgument("name"){defaultValue = "User"})){ backStackEntry ->
                    Greeting(name = backStackEntry.arguments?.getString("name"), navController = navController)
                }
            }
        }
    }
}

